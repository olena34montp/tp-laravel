     */
    public function update(Request $request, $id)
    {
        $jeu = Jeu::find($id);
        $jeu->titre = $request->input('titre');
        $jeu->save();
        return redirect()->route('jeux.edit', ['id'=>$id, 'jeux'=>$jeu]);
    }

